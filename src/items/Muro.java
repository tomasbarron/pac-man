package items;

/**
*Clase que representa a un Muro en el juego
*@author Barron Juan Tomas - Tomasetto Santiago
*/

public class Muro extends Item {

	/**
	 *Constructor de la clase Muro
	 *@param x settea la posicion en x del muro
	 *@param y settea la posicion en y del muro
	 */
	
	public Muro(int x,int y) {
		this.setX(x);
		this.setY(y);
	}

}
